import turtle
import math
def draw_ae(t,r):
    t.fd(5)
    t.bk(55)
    t.rt(90)
    c=math.pi*r
    l=c/50
    for i in range(50):
        t.fd(l)
        t.lt(330/50)
    t.up()
    t.lt(120)
    t.fd(100)
    t.down()
def draw_ii(t):
    t.up()
    t.lt(90)
    t.fd(35)
    t.down()
    t.width(4)
    t.fd(1)
    t.up()
    t.lt(180)
    t.fd(20)
    t.down()
    t.width(1)
    t.fd(55)
    t.lt(90)
    t.up()
    t.fd(50)
    t.lt(90)
    t.down()
def draw_ng(t):
    t.width(1)
    t.fd(55)
    t.bk(27.5)
    t.rt(90)
    t.fd(25)
    t.lt(90)
    t.fd(27.5)
    t.bk(55)
    t.rt(90)
    t.fd(10)
    t.rt(90)
    t.fd(5)
    t.up()
    t.fd(50)
    t.down()
t=turtle.Turtle()
draw_ae(t,50)
draw_ii(t)
draw_ng(t)
turtle.mainloop()
